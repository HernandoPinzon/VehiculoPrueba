    /**
     * @param args the command line arguments
     */
    public static void main(String args[]) {
        VistaPrincipal vista = new VistaPrincipal();
        Vehiculo vehiculo = new Vehiculo();
        RandomAccessFile lector;
        try {
            lector = new RandomAccessFile("src/Datos/datos.txt","r");
            String linea = "";
            String textoCompleto = "";
            while((linea = lector.readLine()) != null){
                textoCompleto = textoCompleto + linea+"\n"; 
            }
            lector.close();
            String [] nuevoTexto = textoCompleto.split("\n");
            String [] llantas = nuevoTexto[0].split(" ");
            String [] motor = nuevoTexto[1].split(" ");
            vehiculo.addComponents(llantas, motor);
        } catch (FileNotFoundException ex) {
            vista.manejarExcepciones("No se encontro el archivo con los datos");
        } catch (IOException ex) {
            vista.manejarExcepciones("Se ha producido un error a la hora de intentar manejar el archivo");
        } catch (LlantasInvalidasException ex) {
            vista.manejarExcepciones(ex);
        } catch (MotorInvalidoException ex) {
            vista.manejarExcepciones(ex);
        } 
        vista.setVehiculo(vehiculo);
        vehiculo.setActualizable(vista);
        vista.setVisible(true);
    }

