    private void bAcelerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAcelerarActionPerformed
        String valorAceleracion = tfValorMovimiento.getText();
        try{
            int velocidad = Integer.parseInt(valorAceleracion.trim());
            vehiculo.acelerar(velocidad);
        }
        catch(java.lang.NumberFormatException e){
            manejarExcepciones("Eso no es un numero valido");
        } catch (AceleradoInvalidoException e) {
            manejarExcepciones(e);
        } catch (VehiculoAccidentadoException e) {
            manejarExcepciones(e);
        } 
    }//GEN-LAST:event_bAcelerarActionPerformed

    private void bFrenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bFrenarActionPerformed
        String valorFrenado = tfValorMovimiento.getText();
        try{
            int velocidad = Integer.parseInt(valorFrenado.trim());
            vehiculo.frenar(velocidad*(-1));
        }
        catch(java.lang.NumberFormatException e){
            manejarExcepciones("Eso no es un numero");
        } catch (FrenadoInvalidoException e) {
            manejarExcepciones(e);
        } catch (VehiculoPantiandoException e) {
            manejarExcepciones(e);
        } catch (FrenadoBruscoInvalidoException e) {
            manejarExcepciones(e);
        } 
    }//GEN-LAST:event_bFrenarActionPerformed

